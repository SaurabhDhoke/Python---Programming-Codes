def Area(radius,pi = 3.14):     # default argument
	ans = 0.0
	ans = pi * radius * radius
	return ans


def main():
	print("Enter radius of circle : ")
	value = float(input())
	
	Ret = 0.0
	Ret = Area(value,7.10)     # positional and default argument
	# Ret = Area(pi = 7.10, radius = value) # keyword argument
	
	print("Area of circle is : ",Ret)
	
	
if __name__ == "__main__":
	main()


