import os

def Fun():
	print("inside Fun")
	print("PID is : ",os.getpid())
	for i in range(5):
		print("Fun : ",i)
	
def Gun():
	print("inside Gun")
	print("PID is : ",os.getpid())
	for i in range(5):
		print("Gun : ",i)
	
def main():
	print("inside main")
	print("PID is : ",os.getpid())
	
	Fun()
	Gun()

if __name__ == "__main__":
	main()























